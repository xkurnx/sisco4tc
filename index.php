     <div class="ts-Default-Link-Style--onwhite clearfix grpelem" id="u187-7"><!-- content -->
      <p>With the <a class="nonblock" href="https://developer.cisco.com/site/collaboration/jabber/websdk/overview/" target="_blank" title="Jabber SDK">Jabber SDK for Web</a>,&nbsp; you can rapidly build Cisco Unified Communications (UC) capabilities of voice, video, instant messaging, presence, voice messaging and conferencing into your web applications and portals using Cisco Jabber technology. Create a powerful unified communications experience with all the functionality seamlessly accessible anywhere, any time.</p>
     </div>
     <div class="ts-Default-Link-Style--onwhite clearfix grpelem" id="u197-10"><!-- content -->
      <p>The <a class="nonblock" href="https://developer.cisco.com/site/networking/mobility/mobility-services/overview/" target="_blank" title="Mobility Services API">Mobility Services API</a>&nbsp;is used to enhance and personalize components of the Cisco <a class="nonblock" href="http://www.cisco.com/en/US/netsol/ns1205/index.html" target="_blank" title="Cisco Connected Mobile Experiences">Connected Mobile Experiences </a>(CMX) solution to help increase visibility into the network, customize location&#45;based mobile services, and strengthen security. Built into the Mobility Services Engine, this API can use real&#45;time intelligence from the Wi&#45;Fi network to&nbsp; connect devices, people and processes with technology to interact more effectively.</p>
     </div>
     <div class="ts-Default-Link-Style--onwhite clearfix grpelem" id="u201-7"><!-- content -->
